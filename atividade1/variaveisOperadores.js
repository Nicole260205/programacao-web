//Faça a declaração de 2 variáveis numéricas para cada operador e guarde o resultado da operação em uma terceira variável;
// Para cada resultado obtido, imprima o texto “Resultado da {operação} entre {num_A} e {num_B}”

//declarar
let num_A = 10;
let num_B = 2;
console.log(`Os números são: ${num_A} e ${num_B}`);

//soma
let soma = num_A + num_B;
console.log(`Resultado da soma entre ${num_A} e ${num_B} = ${soma}`);

//subtração
let sub = num_A - num_B;
console.log(`Resultado da subtração entre ${num_A} e ${num_B} = ${sub}`);

//multiplicação
let mult = num_A * num_B
console.log(`Resultado da multiplicação entre ${num_A} e ${num_B} = ${mult}`);

//divisão
let div = num_A / num_B
console.log(`Resultado da divisão entre ${num_A} e ${num_B} = ${div}`);
